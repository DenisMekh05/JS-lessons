function isSameNum(num1, num2) {
    if (num1 === num2) {
        return true
    }
    else {
        return false
    }
}

function and(param1, param2) {
    if(param1 && param2) {
        return true
    }
    else {
        return false
    }
}

function boolToString(param) {
    return '"' + param + '"'
}

function convert(num1, num2) {
    return (num1 * 3600) + (num2 * 60)
}

function isSeven(num) {
    if(num === 7) {
        return true
    }
    else {
        return false
    }
}