function cubeNumber(number) {
    return number ** 3
}

function arrayGetFirstElement(arrayName) {
    return arrayName[0]
}

function multiply(number1, number2) {
    return number1 * number2
}

function hoursToSeconds(number) {
    return number * 3600
}

function triangleSide(number1, number2) {
    return (number1 + number2) - 1
}